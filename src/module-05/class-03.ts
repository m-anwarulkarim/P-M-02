/* ========================================================
   TypeScript Install & Setup Commands
   ========================================================

   Step 1: Node.js ইন্সটল আছে কিনা চেক করা
   node -v          # Node.js ভার্সন চেক
   npm -v           # npm ভার্সন চেক

   Step 2: TypeScript গ্লোবালি ইন্সটল করা
   npm install -g typescript

   Step 3: TypeScript ভার্সন চেক করা
   tsc -v

   Step 4: প্রজেক্টে TypeScript init করা
   tsc --init

   Step 5: TypeScript ফাইল তৈরি করা
   touch index.ts       # Linux/Mac
   New-Item index.ts -Type File  # Windows PowerShell

   Step 6: TypeScript ফাইল কম্পাইল করা
   tsc index.ts

   Step 7: কম্পাইল হওয়া JS ফাইল রান করা
   node index.js

   Step 8: TypeScript ফাইল স্বয়ংক্রিয়ভাবে কম্পাইল করা
   tsc --watch
   tsc index.ts --watch

   ========================================================
   End of Commands
   ========================================================
*/
